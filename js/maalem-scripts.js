function openNav() {
    document.getElementById("maalem-mobile-nav").style.width = "250px";
    document.getElementById("maalem-wrapper").style.marginRight = "250px";
}

function closeNav() {
    document.getElementById("maalem-mobile-nav").style.width = "0";
    document.getElementById("maalem-wrapper").style.marginRight = "0";
}
